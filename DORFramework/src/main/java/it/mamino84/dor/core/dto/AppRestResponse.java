package it.mamino84.dor.core.dto;

public class AppRestResponse {
	private String id;
	private String html;

	public AppRestResponse() {
		super();
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getHtml() {
		return html;
	}

	public void setHtml(String html) {
		this.html = html;
	}

}
